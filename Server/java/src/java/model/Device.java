package model;

/**
 *
 * @author tung
 */
public class Device {
    String deviceIMEI;
    String deviceName;
    public Device() {
        deviceIMEI = "";
        deviceName = "";
    }

    public String getDeviceIMEI() {
        return deviceIMEI;
    }

    public void setDeviceIMEI(String deviceIMEI) {
        this.deviceIMEI = deviceIMEI;
    }

    public String getDeviceName() {
        return deviceName;
    }

    public void setDeviceName(String deviceName) {
        this.deviceName = deviceName;
    }
    
}
